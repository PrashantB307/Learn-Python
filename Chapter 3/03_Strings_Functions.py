story = "Once upon a time there is a student name Prashant Bhardwaj, who studied in IES College of Technology"

print(len(story))    # ==> 100

print(story.endswith("Technology"))    # ==> True

print(story.count("d"))    # ==> 4

print(story.find("a"))    # ==> 10(index)

print(story.replace("Prashant", "Pawan"))
