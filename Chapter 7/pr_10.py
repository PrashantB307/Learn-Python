



# Print Multipication Table  in Reverse Order by For Loop ======>
# ===============================================================


num = int(input("Enter the Number :- "))
j = 10
for i in range(j , 0 , -1):
    # print(num, " X ", i , " = " , num * i)
    print(f"{num} X {i} = {num * i}")

